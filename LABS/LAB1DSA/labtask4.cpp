// Write code to find the memory in bytes occupied by int, long, double, float and char.

#include <iostream>
using namespace std;

int main()
{
    cout << sizeof(int) << endl;
    cout << sizeof(long) << endl;
    cout << sizeof(double) << endl;
    cout << sizeof(float) << endl;
    cout << sizeof(char) << endl;

    return 0;
}
